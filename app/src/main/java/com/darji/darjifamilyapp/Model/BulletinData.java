package com.darji.darjifamilyapp.Model;

public class BulletinData {
    private int BulletinId;
    private String Title;
    private int Month;
    private int Year;
    private String BulletinPath;
    private int IsActive;

    public int getBulletinId() {
        return BulletinId;
    }

    public String getTitle() {
        return Title;
    }

    public int getMonth() {
        return Month;
    }

    public int getYear() {
        return Year;
    }

    public String getBulletinPath() {
        return BulletinPath;
    }

    public int getIsActive() {
        return IsActive;
    }
}
